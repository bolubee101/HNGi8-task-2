const mongoose = require("mongoose");

let submissionSchema = new mongoose.Schema({
  firstname: {
    type: String,
    required: true,
  },
  lastname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  subject: {
    type: String,
    required: true,
  },
});

let submission = mongoose.model("Submission", submissionSchema);
module.exports = submission;
